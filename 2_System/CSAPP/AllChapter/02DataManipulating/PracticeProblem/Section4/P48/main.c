{
    // 0x   0   0   3   5   9   1   4   1
    //      00000000001101011001000101000001
    // 0x   4   A   5   6   4   5   0   4
    //      01001010010101100100010100000100
    //      00000000001101011001000101000001
    //        01001010010101100100010100000100
    //                 *********************
    // 1101011001000101000001 = 1.101011001000101000001 * 2^21
    // expo = 21 = 148 - 127. 148 = 10010100
    printf("00000000001101011001000101000001\n");
    printf("  01001010010101100100010100000100\n");
    printf("           *********************  \n");
    printf("above show the similarity of fractional field\n");
    printf("in floating representation, 10010100 = 148 -> 148 - 127(bias) = 21\n");
    printf("the int representation = 1.101011001000101000001 * 2^21, so the 21 is correct\n");
}
